    public mostrarActivos() {
        initComponents();
        
        DefaultTableModel tablaModelo = (DefaultTableModel) activos_jTable.getModel();
        activos a = new activos();
        ArrayList<activos> list = a.listaractivo();
        tablaModelo.setRowCount(0);
        
        DefaultTableCellRenderer tcr = new DefaultTableCellRenderer();
        tcr.setHorizontalAlignment(SwingConstants.CENTER);
        activos_jTable.getColumnModel().getColumn(0).setCellRenderer(tcr);
        activos_jTable.getColumnModel().getColumn(1).setCellRenderer(tcr);
        activos_jTable.getColumnModel().getColumn(2).setCellRenderer(tcr);
        activos_jTable.getColumnModel().getColumn(3).setCellRenderer(tcr);
        activos_jTable.getColumnModel().getColumn(4).setCellRenderer(tcr);
        activos_jTable.getColumnModel().getColumn(5).setCellRenderer(tcr);
        activos_jTable.getColumnModel().getColumn(8).setCellRenderer(tcr);
                      
        for(int i = 0; i < list.size(); i++){
            activos temp = list.get(i);
            
            Object row[] = {temp.getPiso(), temp.getBloque(), temp.getCeldas(), temp.getTipo(), temp.getPlaca(), temp.getHoraIngreso(), temp.getNombre(), temp.getApellido(), temp.getId()};
            tablaModelo.addRow(row);
        }
        
        activos_jTable.setModel(tablaModelo);
    }

